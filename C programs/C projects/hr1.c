//sum and difference of two numbers from HackerRank


#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main()
{
	int a, b;
    float c, d;
    scanf("%d %d", &a, &b);
    scanf("%f %f", &c, &d);
    a+b;
    a-b;
    c+d;
    c-d;
    printf("\n%d %d", a+b, a-b);
    printf("\n%f %f", c+d, c-d);
    return 0;
}